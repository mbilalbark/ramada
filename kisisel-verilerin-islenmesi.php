                <h4 class="mini-header">EK – TANIM ve TARİFLER</h4>
                <h5 class="h5-title">1. GİRİŞ</h5>
                <p class="mini-content">
                    Türkiye Cumhuriyeti Anayasası’nın 20. Maddesine göre; herkes, kendisi ile ilgili kişisel verilerin
                    korunmasını talep etme hakkına sahip olduğu bu hakkın; kişinin kendisiyle ilgili kişisel veriler
                    hakkında bilgilendirme, bu verilere erişme, bunların düzeltilmesini veya silmesini talep etme ve
                    amaçları doğrultusunda kullanıp kullanılmadığını öğrenmeyi de kapsamaktadır.
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</br></br>İş bu
                    Anayasal hakkın kullanılması kapsamında, kişisel verilerin işlenmesinde kişilerin temel hak ve
                    özgürlüklerinin korunması ve kişisel verileri işleyen gerçek ve tüzel kişilerin yükümlülükleri ile
                    uyacakları usul ve esasları düzenleyen temel yasal dayanak olarak 6698 Sayılı Kişisel Verilerin
                    Korunması Kanunu yayımlanmıştır.
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</br></br>Kraas Gıda
                    Day. Tük. Malları Teks. Ür. Turizm Otelcilik ve İnş. Taahh. San. Tic. AŞ (Ramada By Wyndham Sakarya)
                    KVK Kanunu’na uyum konusunda gerekli özeni göstermekte ve bunu Kişisel Verilerin İşlenmesi,
                    Saklanması ve İmha Politikası ile bir şirket politikası haline getirmektedir. Bu nedenle veri
                    sorumlusu sıfatıyla firmamızda tüm kişisel veriler 6698 Sayılı Kişisel Verilerin Korunması Kanuna
                    dayalı olarak çıkarılmış olan ve 30224 sayılı Resmî Gazete’ de 28.10.17 tarihinde yayınlanan Kişisel
                    Verilerin Silinmesi, Yok Edilmesi veya Anonim Hale Getirilmesi Hakkında Yönetmelik 5. Ve. 6.
                    Maddeleri gereği kişisel verilerin saklanması ve imhasına ilişkin yükümlülüklerin ve sair mevzuatı
                    uyarınca kişisel verilerin silinmesi, yok edilmesi veya anonim hale getirilmesine ilişkin Ramada By
                    Wyndham Sakarya tarafından uygulanacak usul ve esasların belirlenmesi amacıyla hazırlanmıştır.
                    Kanuni kapsam dışında olan firmamızda KVKK Yönetim Sisteminin uygulanması,
                    iletişim halinde olduğumuz tüm gerçek kişilere duyduğumuz saygı ve verdiğimiz önemden
                    kaynaklanmaktadır.
                </p>
                <h5 class="h5-title">2. AMAÇ VE KAPSAM</h5>
                <p class="mini-content">
                    Hukukun üstünlüğüne olan inancı ile Ramada By Wyndham Sakarya, 6698 Sayılı KVK Kanuna göre uygun
                    olarak yürütülen Kişisel Veri işleme faaliyetleri ve Kişisel Verilerin Korunmasına yönelik olarak
                    benimsenen ilkeler konusunda yükümlülüklere uyumun sağlanması, otelimiz gerçekleştirdiği faaliyetler
                    kapsamında temin edilen kişisel bilgilerin işlenmesi, aktarılması ve gizliliğin korunması ile ilgili
                    hususların risk temelli bir yaklaşımla değerlendirerek, stratejilerin, şirket içi kontrol ve
                    önlemlerin, işleyiş, kural ve sorumlulukların belirlenmesi ile otel çalışanlarının bu konularda
                    bilinçlendirmesidir. Otelimiz tarafından işlenen Kişisel Verileri Kanun hükmüne göre hareket ederek
                    uyum süreci amaçlanmaktadır. İş bu kapsamda; çalışanlarımızın, çalışan adaylarımızın, stajyer
                    adaylarımızın, stajyerlerimizin, otel misafirlerimizin, müşterilerimizin, potansiyel
                    müşterilerimizin, iş ortaklarımızın ve çalışanlarının, şirket yetkililerin ve ortaklarının, aracı
                    kuruluşlar, tedarikçilerimizin, ziyaretçilerin, acentelerin, kamu kurum ve kuruluşları ile özel
                    hukuk tüzel kişilerinin çalışanları, üçüncü kişiler ve otomatik olan ya da herhangi bir veri kayıt
                    sisteminin parçası olmak kaydıyla otomatik olmayan yollarla işlenen tüm kişisel verilerine
                    ilişkindir olarak iş
                    ilişkisi içerisinde bulunduğumuz yurt içi – yurt dışı gerçek kişileri kapsamaktadır.
                    </br></br>Ramada By Wyndham Sakarya, nezdinde kişisel verisi bulunan tüm gerçek kişilerin kişisel
                    verilerini işleme, saklama, koruma ve imha etme çerçevesinde kanunlara uygun olarak Kişisel
                    Verilerin İşlenmesi, Saklanması ve İmha Politikasında belirtildiği gibi yönetilmektedir. Bu
                    politika, KVKK ile ilgili mevzuatın düzenlemelerinin gerektirmesi veya Veri Sorumlusunun gerekli
                    görmesi halinde Veri Sorumlusu tarafından değiştirilebilir.
                </p>
                <h5 class="h5-title">3. POLİTİKA ESASLARI TEMEL VE GENEL İLKELER</h5>
                <p class="mini-content">
                    Ramada By Wyndham Sakarya tarafından, kişisel verilerin korunması mevzuatına uyum sağlanması ve
                    uyumun sürdürülebilirliği için KVK Kanunu’nun 4. Maddesi uyarınca, kişisel verilerin işlenmesinde
                    aşağıdaki ilkelere uyulması zorunludur.

                    </br></br>Hukuka ve dürüstlük kurallarına uygun olma,
                    </br></br>Ramada By Wyndham Sakarya, Türkiye Cumhuriyeti Anayasası başta olmak üzere, kişisel
                    verileri işleme faaliyetlerinde mevzuata uygun olarak hukuka ve dürüstlük kurallarına uygun olarak
                    yürütür.

                    </br></br>Doğru ve gerektiğinde güncel olma,
                    </br></br>Ramada By Wyndham Sakarya, işlenen kişisel verilerin doğruluğunu ve güncelliğini
                    sağlamasına yönelik idari ve teknik tedbirleri alarak, gerekli süreçleri yönetir. Veri sahiplerinin
                    temel haklarını ve kendi meşru menfaatlerini dikkate alarak işlendiği kişisel verilerin doğru ve
                    güncel olmasını sağlar.

                    </br></br>Belirli, açık ve meşru amaçlar için işlenme,
                    </br></br>Ramada By Wyndham Sakarya, kişisel veri işleme amacının açık ve kesin olarak
                    belirlenmesine, işlenen kişisel verilerin yapılan iş veya sunulan hizmetle bağlantılı ve bu amaçlar
                    için gerekli olmasına özen gösterir.

                    </br></br>İşlendikleri amaçla bağlantılı, sınırlı ve ölçülü olma,
                    </br></br>Kişisel veriler, Ramada By Wyndham Sakarya tarafından ilgili amaç ile bağlantılı olarak
                    gerektiği kadar işlenmektedir. Daha sonra kullanabileceği varsayımı ile veri işleme faaliyeti
                    yürütülmemektedir. Amacın gerçekleştirilmesiyle ilgili olmayan veya ihtiyaç duyulmayan kişisel
                    verilerin işlenmesinden kaçınmaktadır. Veri işleme faaliyeti ile gerçekleştirilmesi istenen amaç
                    arasında makul bir dengenin kurulması sağlanmakta böylelikle veri sahiplerinin temel haklarını ve
                    kendi meşru menfaatlerini göz önünde bulundurmaktadır.

                    </br></br>İlgili mevzuatta öngörülen veya işlendikleri amaç için gerekli olan süre kadar muhafaza
                    edilme,
                    </br></br>Ramada By Wyndham Sakarya, tarafından kişisel veriler KVK Kanunu ve ilgili mevzuatta
                    öngörülen veya veri işleme faaliyetine ilişkin amaçların gerektirdiği süre ile sınırlı olarak
                    saklanmaktadır. Türk Ceza Kanunun 138. Maddesi ve KVK Kanunu’nun 4. Ve 7. Maddelerinden kaynaklanan
                    süre sınırına uygun davranmakta, bir süre belirlenmemişse kişisel verileri işlendikleri amaç için
                    gerekli olan süre kadar saklamaktadır. Mevzuatta öngörülen sürenin bitimi veya kişisel verilerin
                    işlenmesini gerektiren sebeplerin ortadan kalkması halinde kişisel verileri silinip, yok edilip veya
                    anonim hale getirilmektedir.

                    </br></br>Kişisel verilerin silinmesi, yok edilmesi ve anonim hale getirilmesinde Kanunun 4.
                    Maddesinde sayılan ilkeler ile 12. Maddesi kapsamında alınması gereken ve iş bu politikada
                    belirtilen idari ve teknik tedbirler, ilgili mevzuat hükümlerine, Kurul kararlarına ve iş bu
                    politikaya tamamen uygun hareket edilmektedir.
                    </br></br>Veri Güvenliğine İlişkin Yükümlülükler (Madde 12)

                    </br></br>“Veri Sorumlusu; Kişisel verilerin hukuka aykırı olarak işlenmesini önlemek, Kişisel
                    verilerin hukuka aykırı olarak erişilmesini önlemek, kişisel verilerin muhafazasını sağlamak,
                    amacıyla uygun güvenlik düzeyini temin etmeye yönelik gerekli her türlü teknik ve idari tedbirleri
                    almak zorundadır. “

                    </br></br>Kişisel verilerin silinmesi, yok edilmesi, anonim hale getirilmesiyle ilgili yapılan tüm
                    işlemler Ramada By Wyndham Sakarya tarafından kayıt altına alınmakta ve söz konusu kayıtlar, hukuki
                    yükümlülük ve belirtilen sürelere göre saklanmaktadır.
                    </br></br>Kurul tarafından aksi bir karar alınmadıkça, kişisel verilerin re ’sen silme, yok etme
                    veya anonim hale getirme yöntemlerinden uygun olanı tarafımızca seçilmektedir. Ancak ilgili kişinin
                    talebi halinde uygun yöntem gerekçesi açıklanarak seçilecektir.
                    </br></br>Kanunun 5. Ve 6. Maddelerinde yer alan kişisel verilerin işlenme şartlarının tamamının
                    ortadan kalkması halinde, kişisel veriler Ramada By Wyndham Sakarya tarafından re’ sen veya ilgili
                    kişini talebi üzerine silinmekte, yok edilmekte veya anonim hale getirilmektedir. Bu hususta ilgili
                    kişi tarafından Ramada By Wyndham Sakarya’ ya başvurulması halinde; iletilen talepler en geç 30
                    (otuz) gün içerisinde sonuçlandırılmakta ve ilgili kişiye bilgi verilmektedir,
                </p>
                <h5 class="h5-title">SAKLAMA VE İMHAYA İLİŞKİN AÇIKLAMALAR</h5>

                <p class="mini-content">
                    </br></br>Veri sahiplerine ait kişisel veriler, Ramada By Wyndham Sakarya tarafından özellikle (i)
                    ticari faaliyetlerin sürdürülebilmesi, (ii) hukuki yükümlülüklerin yerine getirilebilmesi, (iii)
                    çalışan haklarının ve yan haklarının planlanması ve ifası ile (iv) müşteri ilişkilerinin
                    yönetilebilmesi amacıyla yukarıda sayılan fiziki veyahut elektronik ortamlarda güvenli bir biçimde
                    KVKK ve diğer ilgili mevzuatta belirtilen sınırlar çerçevesinde saklanmaktadır.

                    </br></br>Saklamayı gerektiren sebepler aşağıdaki gibidir:

                    </br></br>Kişisel verilerin sözleşmelerin kurulması ve ifası ile doğrudan doğruya ilgili olması
                    nedeniyle saklanması,
                    </br></br>Kişisel verilerin bir hakkın tesisi, kullanılması veya korunması amacıyla saklanması,
                    </br></br>Kişisel verilerin kişilerin temel hak ve özgürlüklerine zarar vermemek kaydıyla Ramada By
                    Wyndham Sakarya’ nın meşru menfaatleri için saklanmasının zorunlu olması,
                    </br></br>Kişisel verilerin Ramada By Wyndham Sakarya’ nın herhangi bir hukuki yükümlülüğünü yerine
                    getirmesi amacıyla saklanması,
                    </br></br>Mevzuatta kişisel verilerin saklanmasının açıkça öngörülmesi,
                    </br></br>Veri sahiplerinin açık rızasının alınmasını gerektiren saklama faaliyetleri açısından veri
                    sahiplerinin açık rızasının bulunması,
                    </br></br>Yönetmelik uyarınca, aşağıda sayılan hallerde veri sahiplerine ait kişisel veriler, Ramada
                    By Wyndham Sakarya tarafından re’ sen yahut talep üzerine silinir, yok edilir veya anonim hale
                    getirilir.

                    </br></br>a) Kişisel verilerin işlenmesine veya saklanmasına esas teşkil eden ilgili mevzuat
                    hükümlerinin değiştirilmesi veya ilgası,

                    </br></br>b) Kişisel verilerin işlenmesini veya saklanmasını gerektiren amacın ortadan kalkması,

                    </br></br>c) Kanun’un 5. ve 6. maddelerindeki kişisel verilerin işlenmesini gerektiren şartların
                    ortadan kalkması.

                    </br></br>d) Kişisel verileri işlemenin sadece açık rıza şartına istinaden gerçekleştiği hallerde,
                    ilgili kişinin rızasını geri alması,

                    </br></br>e) İlgili kişinin, Kanun’un 11. maddesinin (e) ve (f) bentlerindeki hakları çerçevesinde
                    kişisel verilerinin silinmesi, yok edilmesi veya anonim hale getirilmesine ilişkin yaptığı
                    başvurunun veri sorumlusu tarafından kabul edilmesi,

                    </br></br>f) Veri sorumlusunun, ilgili kişi tarafından kişisel verilerinin silinmesi, yok edilmesi
                    veya anonim hale getirilmesi talebi ile kendisine yapılan başvuruyu reddetmesi, verdiği cevabın
                    yetersiz bulunması veya Kanun’da öngörülen süre içinde cevap vermemesi hallerinde; Kurul’a şikâyette
                    bulunulması ve bu talebin Kurul tarafından uygun bulunması,

                    </br></br>g) Kişisel verilerin saklanmasını gerektiren azami sürenin geçmiş olmasına rağmen, kişisel
                    verileri daha uzun süre saklamayı haklı kılacak herhangi bir şartın mevcut olmaması,

                    </br></br>İlgili kişinin hakları MADDE-11 (1) Herkes, veri sorumlusuna başvurarak kendisiyle ilgili;
                    a) Kişisel veri işlenip işlenmediğini öğrenme, b) Kişisel verileri işlenmişse buna ilişkin bilgi
                    talep etme, c) Kişisel verilerin işlenme amacını ve bunların amacına uygun kullanılıp
                    kullanılmadığını öğrenme, ç) Yurt içinde veya yurt dışında kişisel verilerin aktarıldığı üçüncü
                    kişileri bilme, d) Kişisel verilerin eksik veya yanlış işlenmiş olması hâlinde bunların
                    düzeltilmesini isteme, e) 7 nci maddede öngörülen şartlar çerçevesinde kişisel verilerin silinmesini
                    veya yok edilmesini isteme, f) (d) ve (e) bentleri uyarınca yapılan işlemlerin, kişisel verilerin
                    aktarıldığı üçüncü kişilere bildirilmesini isteme, g) İşlenen verilerin münhasıran otomatik
                    sistemler vasıtasıyla analiz edilmesi suretiyle kişinin kendisi aleyhine bir sonucun ortaya
                    çıkmasına itiraz etme, ğ) Kişisel verilerin kanuna aykırı olarak işlenmesi sebebiyle zarara uğraması
